package indi.cloud.view.model;

public class Negative {

    private String data;
    private String execution_target;
    private String case_number;
    private String court_of_execution;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getExecution_target() {
        return execution_target;
    }

    public void setExecution_target(String execution_target) {
        this.execution_target = execution_target;
    }

    public String getCase_number() {
        return case_number;
    }

    public void setCase_number(String case_number) {
        this.case_number = case_number;
    }

    public String getCourt_of_execution() {
        return court_of_execution;
    }

    public void setCourt_of_execution(String court_of_execution) {
        this.court_of_execution = court_of_execution;
    }
}
